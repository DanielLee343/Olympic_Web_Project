package bean;

public class Delegation {
	private int did;
	private String dname;
	private String dflag1;
	private String dflag2;
	private String dintro;

	public int getDid() {
		return did;
	}

	public void setDid(int did) {
		this.did = did;
	}

	public String getDname() {
		return dname;
	}

	public void setDname(String dname) {
		this.dname = dname;
	}

	public String getDflag1() {
		return dflag1;
	}

	public void setDflag1(String dflag1) {
		this.dflag1 = dflag1;
	}

	public String getDflag2() {
		return dflag2;
	}

	public void setDflag2(String dflag2) {
		this.dflag2 = dflag2;
	}

	public String getDintro() {
		return dintro;
	}

	public void setDintro(String dintro) {
		this.dintro = dintro;
	}

}
